
class Rectangle {

	public static void main(String[] args) {
		  int lenght = 10;
		  int width = 20;
		    
		    System.out.println("enter the lenght:"+lenght);
		   

		    System.out.println("enter the width:"+width);
		    
		    int area=lenght*width;

		    int perimeter = 2*(lenght+width);

		    System.out.println("area of rectangle is %d\n"+area);

		    System.out.println("perimeter of rectangle is %d\n"+perimeter);

		    

	}

}
